// This file is an example of how to use variables in TypeScript.

// Similar to JavaScript, TypeScript has three ways to declare variables:
// 1. var
// 2. let
// 3. const

// In general, you may need to supply the type of the variable when declaring it.
// This is because TypeScript is a statically-typed language, meaning that the type of a variable is known at compile-time.

// The var keyword is the oldest way to declare variables in JavaScript.
var my_var: string = 'Hello, world!'

// The let keyword is the newest way to declare variables in JavaScript.
let my_let: string = 'Hello, world!'

// The const keyword is used to declare constants in JavaScript.
const my_const: string = 'Hello, world!'
