// This file is an example of how to use data-types in JavaScript.

// While it is a dynamic programming language, JavaScript does not support type annotations outside of comments.
// This means that you do not need to specify the type of a variable when you declare it.
// With this in mind, JavaScript does have a few different data-types that you should be aware of.

// The first data-type is the string data-type.
// Strings are used to represent text.
// Strings may be declared using single quotes ('), double quotes ("), or backticks (`).
// Strings declared using single quotes or double quotes may not span multiple lines.
// Strings declared using backticks may span multiple lines.
const string_one = 'This is a string.'
const string_two = "This is also a string."
const string_three = `This is a string that spans
multiple lines.`

// The second data-type is the number data-type.
// Numbers are used to represent numbers.
// Numbers may be declared using integers or decimals.
const number_one = 1
const number_two = 1.5

// The third data-type is the boolean data-type.
// Booleans are used to represent true or false values.
// Booleans may be declared using the true or false keywords.
const boolean_one = true
const boolean_two = false

// The fourth data-type is the null data-type.
// Null is used to represent the absence of a value.
// Null may be declared using the null keyword.
const null_value = null

// The fifth data-type is the undefined data-type.
// Undefined is used to represent the absence of a value.
// Undefined may be declared using the undefined keyword.
const undefined_value = undefined

// The sixth data-type is the object data-type.
// Objects are used to represent collections of key-value pairs.
// Objects may be declared using curly braces ({}).
// Objects may be declared using the object literal syntax.
const object_one = {}

// The seventh data-type is the array data-type.
// Arrays are used to represent collections of values.
// Arrays may be declared using square brackets ([]).
// Arrays may be declared using the array literal syntax.
const array_one = []

// The eighth data-type is the function data-type.
// Functions are used to represent reusable blocks of code.
// Functions may be declared using the function keyword.
// Functions may be declared using the arrow function syntax.
// Additionally, functions may be declared using the arrow function expression syntax.
const function_one = function () {}
const function_two = () => {}
