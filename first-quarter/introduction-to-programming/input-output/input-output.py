# This file is an example of how input and output work in Python.

# The input function is used to get input from the user.
# The input function returns a string.
# The input function takes one argument: the prompt to display to the user.
# The agrument is optional
n = int(input())

# The print function is used to display output to the user.
# The print function takes one or more arguments.
# The arguments are optional.
print(n)
