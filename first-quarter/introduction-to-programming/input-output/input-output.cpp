// This file is an example of how to accept input and output in C++.
#include <bits/stdc++.h>

int main() {
    // The std::cin object is used to accept input from the user.
    // It automatically adjusts what it accepts based on the data type of the variable.
    int n; std::cin >> n;

    // The std::cout object is used to output to the user.
    std::cout << n << '\n';
}
